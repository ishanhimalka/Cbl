    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        String nplate = LorryNumberPlateTextField1.getText();
        try {
            boolean isDeleted = Controllorry.Deletelorry(nplate);
            if (isDeleted) {
                DeleteLabel.setText("Deleted ");
                saveLabel.setText("");
            } else {
                DeleteLabel.setText("Delete fail");
                saveLabel.setText("");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void SaveNewProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveNewProductButtonActionPerformed
        String nplate = LorryNumberPlateTextField1.getText();
        String dnic =DriverNicComboBox.getSelectedItem().toString();
        String hnic = HelperNicComboBox.getSelectedItem().toString();
        try {
            boolean isAdded = Controllorry.saveLorry(nplate, dnic, hnic);
            if (isAdded) {
                LorryNumberPlateTextField1.setText("");
                saveLabel.setText("Saved");
                DeleteLabel.setText("");
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_SaveNewProductButtonActionPerformed

