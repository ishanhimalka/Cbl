    private void loadDrivers() throws ClassNotFoundException, SQLException {
        for(MDriverDetails temp: new ControlDriverDetails().getAllDrivers()){
            DriverNicComboBox.addItem(temp.getNic());
        }
      for(MHelperDetails temp: new ControlHelperDetails().getAllHelpers()){
        HelperNicComboBox.addItem(temp.getNic());
       }
    }
