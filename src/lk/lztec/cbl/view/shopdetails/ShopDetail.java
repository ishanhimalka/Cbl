    private void loadshop() throws ClassNotFoundException, SQLException {
        DefaultTableModel model = (DefaultTableModel) ShopTable.getModel();
        List<MShop> list= new ControlShopD().getAllshop();
        Object row[]=new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0]=list.get(i).getId();
            row[1]=list.get(i).getName();
            row[2]=list.get(i).getAddress();
            row[3]=list.get(i).getPhone();
            row[4]=list.get(i).getOmic();
            row[5]=list.get(i).getOname();
            model.addRow(row);
        }
    }
