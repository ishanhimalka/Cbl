    private void BillNoComboBoxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BillNoComboBoxMouseClicked
        int bno = Integer.parseInt(BillNoComboBox.getSelectedItem().toString());
        DefaultTableModel model = (DefaultTableModel) AmountTable.getModel();
        int a = model.getRowCount();
        for (int i = 0; i < a; i++) {
            model.removeRow(0);
        }
        List<MSales> list = null;

        list = new ControlSales().loadsales(bno);

        Object row[] = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getSno();
            row[1] = list.get(i).getLsid();
            row[2] = list.get(i).getBno();
            row[3] = list.get(i).getQty();
            row[4] = list.get(i).getDis();
            row[5] = list.get(i).getTot();
            model.addRow(row);
        }
    }//GEN-LAST:event_BillNoComboBoxMouseClicked

