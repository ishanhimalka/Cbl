    private void TableButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TableButtonActionPerformed
         DefaultTableModel model = (DefaultTableModel) AmountTable.getModel();
        int a = model.getRowCount();
        for (int i = 0; i < a; i++) {
            model.removeRow(0);
        }
        List<MSales> list = null;
       
        try {
            list = new ControlSales().getAllSales();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PreviousBill.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PreviousBill.class.getName()).log(Level.SEVERE, null, ex);
        }

            Object row[] = new Object[6];
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getBno();
                row[1] = list.get(i).getLsid();
                row[2] = list.get(i).getBno();
                row[3] = list.get(i).getQty();
                row[4] = list.get(i).getDis();
                row[5] = list.get(i).getTot();
                model.addRow(row);
            }
    }//GEN-LAST:event_TableButtonActionPerformed

