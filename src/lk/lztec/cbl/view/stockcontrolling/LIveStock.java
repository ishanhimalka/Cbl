    private void qtyButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_qtyButton2ActionPerformed
        DefaultTableModel model = (DefaultTableModel) ProductTable.getModel();
         int a = model.getRowCount();
        for (int i = 0; i < a; i++) {
            model.removeRow(0);
        }
        List<MProductDetails> list = null;
        try {
            list = new ControlProductDetails().loadProductTableq();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LIveStock.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LIveStock.class.getName()).log(Level.SEVERE, null, ex);
        }
        Object row[]=new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0]=list.get(i).getPid();
            row[1]=list.get(i).getName();
            row[2]=list.get(i).getWeight();
            row[3]=list.get(i).getPackets();
            row[4]=list.get(i).getQty();
            row[5]=list.get(i).getBprice();
            model.addRow(row);
        }
    }//GEN-LAST:event_qtyButton2ActionPerformed

