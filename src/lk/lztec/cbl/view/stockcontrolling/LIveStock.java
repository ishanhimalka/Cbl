    private void loadAllItems() throws ClassNotFoundException, SQLException {
        
        DefaultTableModel model = (DefaultTableModel) ProductTable.getModel();
        
        List<MProductDetails> list= new ControlProductDetails().loadProductTable();
        Object row[]=new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0]=list.get(i).getPid();
            row[1]=list.get(i).getName();
            row[2]=list.get(i).getWeight();
            row[3]=list.get(i).getPackets();
            row[4]=list.get(i).getQty();
            row[5]=list.get(i).getBprice();
            model.addRow(row);
        }
    }
