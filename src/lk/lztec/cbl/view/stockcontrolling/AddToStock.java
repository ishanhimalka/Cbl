    private void TableButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TableButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) LsTable.getModel();
        int a = model.getRowCount();
        for (int i = 0; i < a; i++) {
            model.removeRow(0);
        }
        List<MNewProduct> list = null;
       
        try {
            list = new ControlProductDetails().getAllstockadd();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddToStock.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddToStock.class.getName()).log(Level.SEVERE, null, ex);
        }
      
        Object row[] = new Object[4];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getNo();
            row[1] = list.get(i).getProductid();
            row[2] = list.get(i).getQty();
            row[3] = list.get(i).getData();
            model.addRow(row);
        }
    }//GEN-LAST:event_TableButton1ActionPerformed

    private void ProductIdComboBoxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ProductIdComboBoxMouseClicked
        String pid = ProductIdComboBox.getSelectedItem().toString();
        DefaultTableModel model = (DefaultTableModel) LsTable.getModel();
        int a = model.getRowCount();
        for (int i = 0; i < a; i++) {
            model.removeRow(0);
        }
        List<MNewProduct> list = null;

        try {
            list = new ControlProductDetails().loadnewp(pid);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddToStock.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddToStock.class.getName()).log(Level.SEVERE, null, ex);
        }

        Object row[] = new Object[4];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getNo();
            row[1] = list.get(i).getProductid();
            row[2] = list.get(i).getQty();
            row[3] = list.get(i).getData();
            model.addRow(row);
        }
          
    }//GEN-LAST:event_ProductIdComboBoxMouseClicked

