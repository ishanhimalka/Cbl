    private void SaveNewProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveNewProductButtonActionPerformed
        String pid = ProductIdComboBox.getSelectedItem().toString();
        int qty = Integer.parseInt(QtyTextField.getText());

        boolean isAdded;
        try {
            isAdded = ControlProductDetails.enterNewItem(pid, qty);
            boolean isUpdate = ControlProductDetails.updateqty(pid, qty);
            if (isAdded) {
                QtyTextField.setText("");
                saveLabel.setText("Saved");
                TableButton1.doClick();
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddToStock.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddToStock.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_SaveNewProductButtonActionPerformed

