    private void qtyButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_qtyButton2ActionPerformed
        DefaultTableModel model = (DefaultTableModel) LorrySTable.getModel();
        int a = model.getRowCount();
        for (int i = 0; i < a; i++) {
            model.removeRow(0);
        }
        List<MLorryStock> list = null;
        try {
            list = new ControlLorryStock().loadlsTableq();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LIveStock.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LIveStock.class.getName()).log(Level.SEVERE, null, ex);
        }
        Object row[] = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getLsid();
            row[1] = list.get(i).getLnplate();
            row[2] = list.get(i).getPid();
            row[3] = list.get(i).getSprice();
            row[4] = list.get(i).getQty();
            model.addRow(row);
        }
    }//GEN-LAST:event_qtyButton2ActionPerformed

